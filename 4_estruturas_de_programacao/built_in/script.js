// MATH.X função utilizada para expressão / cálculos matemáticos (Math=objeto | o que vem depois do ponto=função).
let maiorNumero = Math.max(1,4,8,5);
let menorNumero = Math.min(5, 8, 1, 40, 97);
let arredondar = Math.round(5.48915161);
let arredondarParaCima = Math.ceil(5.48915161);
console.log(`O maior número é ${maiorNumero} e o menor é ${menorNumero}.\n
O valor arredondado é ${arredondar} e o valor arredondado para cima é ${arredondarParaCima}.`);

